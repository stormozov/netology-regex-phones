/**
 * Функция phoneFormatter принимает номер телефона и возвращает его в отформатированном формате.
 * 
 * Например:
 * ```
 * phoneFormatter('1234567890'); // '+1234567890'
 * phoneFormatter('8 (927) 000-00-00'); // '+79270000000'
 * phoneFormatter('+7 960 000 00 00'); // '+79600000000'
 * phoneFormatter('+86 000 000 0000'); // '+860000000000'
 * ```
 */
export const phoneFormatter = (phone) => {
  // Алгоритм решения:
  // Очистка номера: Удалить все нецифровые символы из введенной строки.
  // Проверка формата:
  // — Если номер начинается с 8 и имеет длину 11 цифр(российский номер в формате 8XXXXXXXXXX), 
  //   заменить первую 8 на 7 и добавить + в начало.
  // — В остальных случаях просто добавить + в начало очищенного номера.
  
  // Удаляем все нецифровые символы
  const cleaned = phone.replace(/\D/g, '');

  // Проверяем, является ли номер российским (начинается с 8 и длина 11)
  if (cleaned.startsWith('8') && cleaned.length === 11) {
    return '+7' + cleaned.slice(1);
  }

  // Добавляем "+" ко всем остальным номерам
  return '+' + cleaned;
};
